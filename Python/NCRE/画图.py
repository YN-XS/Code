import turtle
turtle.setup(1000, 800, 100, 100)
turtle.penup()
turtle.setheading(180)
turtle.forward(200)
turtle.width(30)
turtle.color(0, 0, 1)
turtle.pendown()
turtle.left(180)
turtle.forward(200)
turtle.setheading(-40)
for i in range(6):
    turtle.circle(20, 80)
    turtle.circle(-20, 80)
turtle.setheading(0)
turtle.forward(40)
turtle.circle(20, 90)
turtle.forward(30)
turtle.circle(20, 90)
turtle.forward(100)
turtle.done()
